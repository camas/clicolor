# 256 Colors
class CLI256:
    @staticmethod
    def fg(value: int) -> str:
        """
        Returns the foreground escape code for a 256 color id.
        See https://jonasjacek.github.io/colors/ for full list.
        """
        return f"\033[38;5;{value}m"

    @staticmethod
    def bg(value: int) -> str:
        """
        Returns the background escape code for a 256 color id.
        See https://jonasjacek.github.io/colors/ for full list.
        """
        return f"\033[48;5;{value}m"

    @staticmethod
    def from_rgb(r: int, g: int, b: int) -> str:
        # https://stackoverflow.com/a/26665998

        if r == g and g == b:
            if r < 8:
                ansii_val =  16
            elif r > 248:
                ansii_val =  231
            else:
                ansii_val =  round(((r - 8) / 247) * 24) + 232
        else:
            ansii_val = 16 \
                + (36 * round(r / 255 * 5)) \
                + (6 * round(g / 255 * 5)) \
                + round(b / 255 * 5)
        return f"\033[38;5;{ansii_val}m"

